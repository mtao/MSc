/*
 * 0 0 0 :0
 * 0 1 0 :1
 * 1 1 0 :2
 * 1 0 0 :3
 * 0 0 1 :4
 * 0 1 1 :5
 * 1 1 1 :6
 * 1 0 1 :7
 * 0-6
 * 1-7
 * 2-4
 * 3-5
 */
//Iterate through every voxel and creat the triangles described in m_triTable
    template <typename GT>
void MarchingCubeGrid<GT>::populateMesh(
        Mesh<NumTraits> & mesh, Scalar level)
{
    m_edgeData.resize(m_NI,m_NJ,m_NK);
    m_edgeData.clear();


    Face f;
    uint i,j,k,l;
    uint edgeInd;
    //N?-1 because there are N?-1 cells
    for(k=0; k<m_NK-1; ++k)
        for(j=0; j<m_NJ-1; ++j)
            for(i=0; i<m_NI-1; ++i)
            {
                localEdgeIndMask(i,j,k,edgeInd,level);

                for(l=0; m_triTable[edgeInd][l]!=-1; l+=3)
                {
                    f[0]=edgeIndex(mesh, i,j,k,m_triTable[edgeInd][l]     ,level);
                    f[1]=edgeIndex(mesh, i,j,k,m_triTable[edgeInd][l+1]   ,level);
                    f[2]=edgeIndex(mesh, i,j,k,m_triTable[edgeInd][l+2]   ,level);
                    mesh.faces.push_back(f);
                }


            }


}




//Edge labeling:
/*
 * 0    : 0 1 0
 * 1    : 1 2 0
 * 2    : 2 1 0
 * 3    : 1 0 0
 * 4    : 0 1 2
 * 5    : 1 2 2   
 * 6    : 2 1 2   
 * 7    : 1 0 2   
 * 8    : 0 0 1
 * 9    : 0 2 1
 * 10   : 2 2 1
 * 11   : 2 0 1
 */
//map masks to index triplets
    template <typename GT>
inline void MarchingCubeGrid<GT>::shiftPos(Index3 & v, char mask)
{
    if(mask & 1) ++v[0];
    if(mask & 2) ++v[1];
    if(mask & 4) ++v[2];
}

    template <typename GT>
inline void MarchingCubeGrid<GT>::localEdgeIndMask(uint i, uint j, uint k, uint & edgeInd, Scalar level)
{
    edgeInd=0;
    if((*this)(i  ,j  ,k  )<level) edgeInd |= 1;
    if((*this)(i  ,j+1,k  )<level) edgeInd |= 2;
    if((*this)(i+1,j+1,k  )<level) edgeInd |= 4;
    if((*this)(i+1,j  ,k  )<level) edgeInd |= 8;
    if((*this)(i  ,j  ,k+1)<level) edgeInd |= 16;
    if((*this)(i  ,j+1,k+1)<level) edgeInd |= 32;
    if((*this)(i+1,j+1,k+1)<level) edgeInd |= 64;
    if((*this)(i+1,j  ,k+1)<level) edgeInd |= 128;
}

    template <typename GT>
inline void MarchingCubeGrid<GT>::edgeVertMasks(uint edgeNo, char &lmask, char &umask)
{
    //Identify which vertices are involved w.r.t to i,j,k
    switch(edgeNo)
    {
        case 0:
            //0 0 0 => 0 1 0
            umask=2;
            break;
        case 1:
            //0 1 0 => 1 1 0
            lmask=2;
            umask=3;
            break;
        case 2:
            //1 1 0 => 1 0 0
            lmask=1;
            umask=3;
            break;
        case 3:
            //1 0 0 => 0 0 0
            umask=1;
            break;
        case 4:
            //0 0 1 => 0 1 1
            lmask=4;
            umask=6;
            break;
        case 5:
            //0 1 1 => 1 1 1
            lmask=6;
            umask=7;
            break;
        case 6:
            //1 1 1 => 1 0 1
            lmask=5;
            umask=7;
            break;
        case 7:
            //1 0 1 => 0 0 1
            lmask=4;
            umask=5;
            break;
        case 8:
            //0 0 0 => 0 0 1
            umask=4;
            break;
        case 9:
            //0 1 0 => 0 1 1
            lmask=2;
            umask=6;
            break;
        case 10:
            //1 1 0 => 1 1 1
            lmask=3;
            umask=7;
            break;
        case 11:
            //1 0 0 => 1 0 1
            lmask=1;
            umask=5;
            break;
    }
}

//returns the vert index for the edge edgeNo for voxel i,j,k
//  (it also creates the vertex if it doesn't exist yet)
    template <typename GT>
uint MarchingCubeGrid<GT>::edgeIndex(
        Mesh<NumTraits> & mesh, 
        uint i, uint j, uint k, uint edgeNo, Scalar level)
{
    std::vector<Vector3> & verts = mesh.vertices;
    //lind = lower index in lexicographical order
    //uind = upper index in lexicographical order
    //dind = uind - lind
    //?mask = more compact notation for ind as a bitmask
    char lmask=0,umask=0,dmask=0;
    Index3 dind;
    dind[0]=dind[1]=dind[2]=0;
    Index3 lind, uind;
    uind[0]=lind[0]=i;
    uind[1]=lind[1]=j;
    uind[2]=lind[2]=k;

    edgeVertMasks(edgeNo,lmask,umask);

    //edgePtr = pointer to vert index for the vertex on that particular edge
    //  Edges are identified by the lower index and dimension they lie on
    int * edgePtr=0;
    dmask=umask-lmask;
    shiftPos(lind,lmask);
    switch(dmask)
    {
        case 1:
            edgePtr=&m_edgeData.u(lind);
            break;
        case 2:
            edgePtr=&m_edgeData.v(lind);
            break;
        case 4:
            edgePtr=&m_edgeData.w(lind);
            break;
    }
    //If this edge doesn't have a vertex associated with it yet and the edge is valid
    //  create one
    if((*edgePtr)==-1 && edgeNo!=-1)
    {
        shiftPos(uind,umask);
        shiftPos(dind,dmask);
        //lerp to find where the 0 isosurface must lie
        //alpha=(0-v_1)/(v_2-v_1)
        Scalar lphi=(*this)(lind[0],lind[1],lind[2]);
        Scalar uphi=(*this)(uind[0],uind[1],uind[2]);
        Scalar alpha=(level-lphi)/(uphi-lphi);

        //create the vertex
        Vector3 v(lind[0],lind[1],lind[2]);
        v+=alpha*Vector3(dind[0],dind[1],dind[2]);
        v[0]*=m_NI;
        v[1]*=m_NJ;
        v[2]*=m_NK;
        verts.push_back(v);
        //this edge corresponds to the vertex at the end
        (*edgePtr)=verts.size()-1;

    }
    return (*edgePtr);
}

