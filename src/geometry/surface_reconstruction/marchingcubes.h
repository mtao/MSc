template <typename GT=GridTraitsf >
class MarchingCubeGrid: public LevelSet<GT>
{
    public:
        typedef IndexSet<3> Index3;
        typedef Grid<GT> GridParent;
        typedef typename GT::Scalar Scalar;
        typedef NumericalTraits<Scalar> NumTraits;
        typedef typename NumTraits::Vector3 Vector3;
        using GridParent::m_NI;
        using GridParent::m_NJ;
        using GridParent::m_NK;
        using GridParent::val;
        using GridParent::read;
        using GridParent::write;
        using GridParent::operator();
        MarchingCubeGrid() {}
        MarchingCubeGrid(uint ni, uint nj, uint nk): LevelSet<GT>(ni,nj,nk){}
        MarchingCubeGrid(const LevelSet<GT> & phi_): LevelSet<GT>(phi_) {}
        void populateMesh(
                Mesh<NumTraits> & mesh, Scalar level);
        Mesh<NumTraits> * genMesh(Scalar level)
        {
            Mesh<NumTraits> * mesh = new Mesh<NumTraits>();
            populateMesh(*mesh,level);
            return mesh;
        }
        static const unsigned int m_edgeTable[256];
        static const int m_triTable[256][16];
    private:
        EdgeStorage<int> m_edgeData;



        inline void shiftPos(Index3 & v, char mask);
        uint edgeIndex(Mesh<NumTraits> & mesh, uint i, uint j, uint k,uint edgeNo, Scalar level);
        uint localEdgeInd(uint i, uint j, uint k,uint &edgeNo, Scalar level);
        inline void localEdgeIndMask(uint i, uint j, uint k, uint & edgeInd, Scalar level);
        inline void edgeVertMasks(uint edgeNo, char &lmask, char &umask);
};

